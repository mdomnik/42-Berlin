int	column_len(char **nb)
{
	int	i;

	i = 0;
	while (nb[0][i] != '\0')
	{
		i++;
	}
	return (i - 1);
}

int	row_len(char **mtrx)
{
	int	i;

	i = 0;
	while (mtrx[i])
	{
		i++;
	}
	return (i);
}
